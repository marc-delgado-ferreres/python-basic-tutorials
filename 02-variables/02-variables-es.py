### Variables ###

# Qué función tienen #

# Las variables se utilizan para almacenar y manipular datos asignando un nombre a un valor.

# Tipos de variables #

# Existen diferentes tipos de variables.

# Variable de tipo entero [int] #

# Esta variable únicamente puede ser un número entero.

entero = 10
print(entero)
print("El resultado es", entero)
print("El resultado es {}".format(entero))
print(f"El resultado es {entero}")

# En este ejemplo, se crea una variable llamada "entero" y se le asigna el número 10.
# Luego, se utiliza la función "print()" para mostrar el contenido de la variable "entero" en la consola.
# Como resultado, se imprimirá "10".
# Como resultado, se imprimirá "El resultado es 10".
# Como resultado, se imprimirá "El resultado es 10".
# Como resultado, se imprimirá "El resultado es 10".

